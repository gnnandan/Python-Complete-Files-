# list
x=[10,200,20,30,10,20,400,50]

# len()-->check the length of the list
print(f"The length of list {len(x)}")

# min()-->used to get minimum element
print(f"The minimum element of list {min(x)}")

# max()-->used to get maximum element
print(f"The maximum element of list {max(x)}")

# sum()-->used to get sum of element in list
print(f"The sum of elements of list {sum(x)}")

# we can also get average of list sum(x)/len(x)
print(f"The average of list elements is {sum(x)/len(x)}")