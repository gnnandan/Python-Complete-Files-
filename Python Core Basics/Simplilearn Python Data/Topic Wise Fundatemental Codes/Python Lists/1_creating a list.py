# creating a list
# list of numbers
x=[1,2,3,4,5,6,7]
print(f"List of numbers: {x} and {type(x)}")

# list of letters
x=['a','b','c','d']
print(f"List of letters: {x} and {type(x)}")

# list of strings
x=['aa1','bb2','cc3','dd4']
print(f"List of string: {x} and {type(x)}")

#list of combinations of all
x=[1,2,'a','b','aa1','bb2']
print(f"List of combinations: {x} and {type(x)}")

#list of list {Matrix}
x=[[1,2],['a','b']]
print(f"List in List(Matrix): {x} and {type(x)}")