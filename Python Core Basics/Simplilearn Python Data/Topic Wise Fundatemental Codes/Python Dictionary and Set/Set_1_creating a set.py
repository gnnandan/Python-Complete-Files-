# creating a set
s={'one','two','three','four','five','six','seven','eight'}
print(f"it is {type(s)} and {s}")

# or

s=set(['a','b','c','d','e','f'])
print(f"it is {type(s)} and {s}")

print()
# creating a frozenset(immutable)
fs=frozenset(['a','b','c','d','e','f'])
print(f"The frozenset is {type(fs)} and {fs}")
